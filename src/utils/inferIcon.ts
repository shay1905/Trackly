export function inferIcon(name: string): string {
  if (!name.trim()) return '🏷️';
  const lower = name.toLowerCase();
  for (const rule of RULES) {
    if (rule.keywords.some((kw) => lower.includes(kw.toLowerCase()))) {
      return rule.icon;
    }
  }
  return '🏷️';
}

/**
 * Infer icon for a subcategory.
 * Falls back to the parent category's icon if no keyword match is found.
 */
export function inferSubcategoryIcon(name: string, parentIcon: string): string {
  const inferred = inferIcon(name);
  if (inferred !== '🏷️') return inferred;
  return parentIcon || '🏷️';
}
